// ============================================================
// TEMP DIR TRACKING
// ============================================================

const tmpDirs: string[] = [];

async function makeTmpDir(): Promise<string> {
  const dir = await mkdtemp(path.join(os.tmpdir(), 'rill-bundle-build-test-'));
  tmpDirs.push(dir);
  return dir;
}

// Save the real cwd before each test and restore it in afterEach so that
// buildPackage's internal process.chdir() does not leave the process in a
// deleted tmpdir when afterEach cleans up.
let savedCwd = process.cwd();

beforeEach(() => {
  savedCwd = process.cwd();
});

afterEach(async () => {
  process.chdir(savedCwd);
  for (const dir of tmpDirs.splice(0)) {
    await rm(dir, { recursive: true, force: true }).catch(() => undefined);
  }
});

